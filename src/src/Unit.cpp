#include "Unit.h"

///////////////////////////////////////////////////////////////////////////////
// Constructor and deconstructor
///////////////////////////////////////////////////////////////////////////////
Unit::Unit() {
	
}

Unit::Unit(Map* map, float x, float y, Stats s)
	: Object(map, x, y, s) {

	_shape.setFillColor(sf::Color::Blue);
	_shape.setPosition(x, y);
}

Unit::~Unit() {

}

////////////////////////////////////////////////////////////////////////////////
// Methods
////////////////////////////////////////////////////////////////////////////////
void Unit::draw(sf::RenderTarget& target, sf::RenderStates stats) const {
	target.draw(_shape);
}
