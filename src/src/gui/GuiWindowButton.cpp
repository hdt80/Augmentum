#include "gui/GuiWindowButton.h"

#include "Game.h"

////////////////////////////////////////////////////////////////////////////////
// Ctor and dtor
////////////////////////////////////////////////////////////////////////////////

GuiWindowButton::GuiWindowButton(const GuiEntryStyle* style, Vector2 orig,
		const std::string& msg, Window* window)
	: GuiButton(style, orig, msg),
		_window(window) {

}

GuiWindowButton::~GuiWindowButton() {
	_window = nullptr;
}

////////////////////////////////////////////////////////////////////////////////
// Methods
////////////////////////////////////////////////////////////////////////////////

void GuiWindowButton::onClick() {
	Game::followWindow(_window);
}
